# !/bin/bash

# =================================================================
# Explore the Broken Configuration
# =================================================================
kubectl get pods
kubectl get deployment redis-deployment -o yaml > fix-redis.yaml
cat fix-redis.yaml

# =================================================================
# Fix the Image Typo
# =================================================================
sed -i 's/redis:alpin/redis:alpine/g' fix-redis.yaml

# =================================================================
# Fix the ConfigMap Typo
# =================================================================
sed -i 's/redis-conig/redis-config/g' fix-redis.yaml

# =================================================================
# Apply the Fixed Configuration
# =================================================================
kubectl apply -f fix-redis.yaml

# =================================================================
# Verify
# =================================================================
kubectl get pods